// Aritméticos
/*
5 + 2 = 7 (Adição)
5 - 2 = 3 (Subtração)
5 * 2 = 10 (Multiplicação)
5 / 2 = 2.5 (Divisão)
5 % 2 = 1 (Resto da divisão inteira)
5 ** 2 = 25 (Potênciação)
*/

// Precedência de operadores
/*
- Quanto uma mesma expressão tem soma e divisão nós fazemos primeiro a divisão
5 + 3 / 2 =
5 + 1.5 =
6.5

- Com a utilização dos parêntesis você muda a ordem de precedência
(5 + 3) / 2 = 
8 / 2 =
4

- Ordem de precedência dos operadores:
() -> Parêntesis
** -> Potênciação
* / % -> Multiplicação, Divisão e Resto da divisão inteira
+ - -> Adição e Subtração

- Atribuição Simples:
var a = 5 + 3 (8)
var b = a % 5 (3)
var c = 5 * b ** 2 ( 5 * 9 = 45)
var d = 10 - a / 2 (10 - 4 = 6)
var e = 6 * 2 / d (12 / 6 = 2)
var f = b % e + 4 / e (1 + 2 = 3)

- Auto-atribuições
var n = 3
n = n + 4 (3 + 4 = 7)
n = n - 5 (7 - 5 = 2)
n = n * 4 (2 * 4 = 8)
n = n / 2 (8 / 2 = 4)
n = n ** 2 ( 4 ** 2 = 16)
n = n % 5 (16 % 5 = 1)

- Simplificando:
n += 4
n -= 5
n *= 4
n /= 2
n **= 2
n %= 5

- Incremento:
var x = 5
x = x + 1 (5 + 1 = 6)   x+= 1       x++
x = x - 1 (6 - 1 = 5)   x -= 1      x--
*/
