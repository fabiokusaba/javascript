// Estrutura de repetição while com teste lógico no início
var c = 1
while (c <= 6) {
  console.log(`Passo ${c}`)
  c++ // c = c + 1
}

// Estrutura de repetição com teste lógico no fim
var cont = 1
do {
  console.log(`Contei ${cont}`)
  cont++
} while(cont <= 6)

